public class AssignDemo
{
    public static void main(String[] args)
    {
        //code here
        int x=3;
        int y=2;
        x+=y;
        System.out.println("result of x: "+x);
        x-=y;
        System.out.println("result of x: "+x);
        x*=y;
        System.out.println("result of x: "+x);
        x/=y;
        System.out.println("result of x: "+x);
        x%=y;
        System.out.println("result of x: "+x);
    }
}